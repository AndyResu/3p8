MICRO_ARTIFACT_SPAWN_TIME = 120

timer.Create("micro_artifact_control",5,0, function()
	for i=1,artifact_count do
		if not IsValid(MICRO_ARTIFACTS[i]) then
			local spawn = table.Random(spawns)

			local name = spawn[1]
			local pos = spawn[2]+Vector(0,0,16)

			local r = 16
			local tr = util.TraceHull{start=pos,endpos=pos,maxs=Vector(1,1,1)*r,mins=Vector(-1,-1,-1)*r}
			if (tr.Hit) then continue end
			
			BroadcastComms(Color(255,0,255),"CENTRAL >>> NEW ARTIFACT @ "..string.upper(name).." IN "..MICRO_ARTIFACT_SPAWN_TIME.." SEC.")

			local art = ents.Create("micro_artifact")
			art:SetPos(pos)
			art:Spawn()
			MICRO_ARTIFACTS[i] = art
			break
		end
	end
end)