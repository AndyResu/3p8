ENT.Type = "anim"

ENT.ItemName = "I.T. Shield"
ENT.ItemModel = "models/hunter/misc/sphere2x2.mdl"
local Power = 1

function ENT:Initialize()
	self:SetModel(self.ItemModel)
	self:PhysicsInitStandard()
    self:SetMaterial("models/spawn_effect2")
    self:SetColor(Color(255,255,255,42))
    self:SetCollisionGroup(2)
end

if SERVER then
    function ENT:SetPower(powerlevel)
        self.Power = powerlevel
    end

    function ENT:OnTakeDamage(damageto)
        self.Power = self.Power - damageto:GetDamage()
    end

    function ENT:GetPower()
        return self.Power
    end
end